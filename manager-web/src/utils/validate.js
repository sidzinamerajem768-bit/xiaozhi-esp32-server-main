/**
 * 手机号格式验证
 * @param {string} mobile 手机号
 * @param {string} areaCode 区号（如 '+86'）
 * @returns {boolean}
 */
export function validateMobile(mobile, areaCode) {
  const cleanMobile = mobile.replace(/\D/g, '')

  const rules = {
    '+86': /^1[3-9]\d{9}$/,
    '+852': /^[569]\d{7}$/,
    '+853': /^6\d{7}$/,
    '+886': /^9\d{8}$/,
    '+1': /^[2-9]\d{9}$/,
    '+44': /^7[1-9]\d{8}$/,
    '+81': /^[7890]\d{8}$/,
    '+82': /^1[0-9]\d{7}$/,
    '+65': /^[89]\d{7}$/,
    '+61': /^[4578]\d{8}$/,
    '+49': /^1[5-7]\d{8}$/,
    '+33': /^[67]\d{8}$/,
    '+39': /^3[0-9]\d{8}$/,
    '+34': /^[6-9]\d{8}$/,
    '+55': /^[1-9]\d{10}$/,
    '+91': /^[6-9]\d{9}$/,
    '+971': /^[5]\d{8}$/,
    '+966': /^[5]\d{8}$/,
    '+880': /^1[3-9]\d{8}$/,
    '+234': /^[789]\d{9}$/,
    '+254': /^[17]\d{8}$/,
    '+255': /^[67]\d{8}$/,
    '+7': /^[67]\d{9}$/
  }

  const rule = rules[areaCode]
  if (rule) return rule.test(cleanMobile)

  // 其他国际号码：5-15 位数字
  return /^\d{5,15}$/.test(cleanMobile)
}